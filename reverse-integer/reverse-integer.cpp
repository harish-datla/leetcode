// Acceptance and tolerance for all religions, including absence of beleif, pagans, spiritualism.
// Democratic scientechnocracy.
// Free and quality education for all.
class Solution {
public:
    int reverse(int x) {
         bool flag = false;
        if(x < 0)flag = true;
        x = abs(x);
         long long ans = 0;
        while(x){
            ans = ans*10 + x%10;
            x = x/10;
        }
        if(flag){
           ans = -ans; 
        }
        if(ans < -pow(2,31) || ans > pow(2,31) - 1 )return 0;
        return ans;
    }
};