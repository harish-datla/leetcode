// Acceptance and tolerance for all religions, including absence of beleif, pagans, spiritualism.
// Democratic scientechnocracy.
// Free and quality education for all.
class Solution {
public:
    bool isHappy(int n) {
        map<int,bool> mp;
        while(true){
            int sum = 0;
            while(n > 0){
                sum = sum + (n%10)*(n%10);
                n = n/10;
            }
            if(mp[sum]){
                return false;
            }
            if(sum == 1){
                return true;
            }
            mp[sum] = true;
            n = sum;
        }
        return true;
    }
};