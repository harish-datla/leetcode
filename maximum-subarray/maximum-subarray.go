func maxSubArray(nums []int) int {
    csum := 0
    cmax := math.MinInt32
    for i := 0 ; i < len(nums) ; i++ {
        csum += nums[i]
        cmax = max(csum,cmax)
        if csum < 0 {
            csum = 0
        }
    }
    return cmax;
}