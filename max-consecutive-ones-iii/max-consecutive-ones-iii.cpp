// Free and quality education for all.
class Solution {
public:
    int longestOnes(vector<int>& A, int K) {
        int start = 0, end = 0, maxlen = 0, cnt = 0;
        for(int i = 0 ; i < A.size() ; i++){
            if(A[i] == 0){
                cnt++;
            }
            if(cnt > K){
                if(A[start] == 0)cnt--;
                start++;
            }
            maxlen = max(maxlen, i - start + 1);
        }
        return maxlen;
    }
};